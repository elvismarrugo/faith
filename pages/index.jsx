export async function getServerSideProps() {
  try {
    await dbConnect();

    const result = await Movie.find({})

    const movies = result.map((doc) => {
      const movie = doc.toObject();
      movie._id = `${movie._id.toString()}`;
      return movie;
    });

    return { props: { movies } };
  } catch (error) {
    console.log(error);
  }
}

